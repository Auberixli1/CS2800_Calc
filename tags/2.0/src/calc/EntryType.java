package calc;

/**
 * This is list of possible types of the entry.
 *
 * @author Marcus Messer
 */
public enum EntryType {
  /**
   * The enumeration for when the entry is a number.
   */
  NUMBER,
  /**
   * The enumeration for when the entry is a symbol.
   */
  SYMBOL,
  /**
   * The enumeration for when the entry is a string.
   */
  STRING,
  /**
   * The enumeration for when the the entry is invalid.
   */
  INVALID
}


