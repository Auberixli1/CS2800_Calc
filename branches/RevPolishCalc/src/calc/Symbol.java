package calc;

/**
 * This is the list of possible symbols.
 *
 * @author Marcus Messer
 */
public enum Symbol {
  /**
   * The enumeration for '('.
   */
  LEFT_BRACKET,

  /**
   * The enumeration for ')'.
   */
  RIGHT_BRACKET,
  /**
   * The enumeration for '*'.
   */
  TIMES,
  /**
   * The enumeration for '/'.
   */
  DIVIDE,
  /**
   * The enumeration for '+'.
   */
  PLUS,
  /**
   * The enumeration for '-'.
   */
  MINUS,
  /**
   * The enumeration for and INVALID symbol.
   */
  INVALID

}
